import matplotlib.pyplot as plt
# 此文件用于标注客户点地理位置
# ==========================================
# 0. 解决 Matplotlib 中文显示乱码和负号问题
# ==========================================
# Windows 用户通常使用 'SimHei' (黑体)，Mac 用户可使用 'Arial Unicode MS'
plt.rcParams['font.sans-serif'] = ['SimHei']  
plt.rcParams['axes.unicode_minus'] = False

# ==========================================
# 1. 输入数据
# ==========================================
# 经纬度字典数据
locations = {
    0:(113.5765967,34.8941855), 1:(113.5561377,34.7409250), 2:(113.6140946,34.7551608),
    3:(113.5512827,34.8198692), 4:(113.6623274,34.7548116), 5:(113.6497080,34.8608752),
    6:(113.8006427,34.7924848), 7:(113.8981885,34.7805231), 8:(113.6867657,34.6205619),
    9:(113.7047691,34.6058205), 10:(113.6715036,34.6890026), 11:(113.6931873,34.7242179),
    12:(113.7462298,34.7429335), 13:(113.6639847,34.8033946), 14:(113.6662585,34.7836224),
    15:(113.6052876,34.8607039), 16:(113.7593603,34.7686597), 17:(113.7130126,34.7713199),
    18:(113.6750943,34.7864745), 19:(113.6572006,34.8075965), 20:(113.6514886,34.8073216),
    21:(113.6210658,34.7409270), 22:(113.6931797,34.7545588), 23:(113.6242167,34.8310125),
    24:(113.6378998,34.8612780), 25:(113.5346314,34.8154432), 26:(113.7289247,34.7642198),
    27:(113.7290246,34.7487973),
}

# ==========================================
# 2. 画图设置
# ==========================================
# 创建画布，设置大小和分辨率(dpi=300适合论文打印)
fig, ax = plt.subplots(figsize=(12, 8), dpi=150)

# ==========================================
# 3. 绘制节点 (散点图)
# ==========================================
for node, (lon, lat) in locations.items():
    if node == 0:
        # 配送中心：红色五角星，调大尺寸
        ax.scatter(lon, lat, c='red', s=250, marker='*', zorder=5, label='配送中心')
        # 在五角星旁边标注'0'
        ax.text(lon + 0.003, lat + 0.003, '0', fontsize=14, fontweight='bold')
    else:
        # 客户点：蓝色圆点
        ax.scatter(lon, lat, c='blue', s=40, zorder=4,label='客户点')
        # 在圆点旁边标注客户ID
        ax.text(lon + 0.002, lat + 0.002, str(node), fontsize=10, color='black')

# ==========================================
# 5. 图表美化与输出
# ==========================================
# 大河四季冷链物流公司客户位置分布
ax.set_title('', fontsize=16, fontweight='bold', pad=15)
ax.set_xlabel('经度 (Longitude)', fontsize=12)
ax.set_ylabel('纬度 (Latitude)', fontsize=12)

# 添加背景网格线
ax.grid(True, linestyle='--', alpha=0.5)

# 合并并显示图例
handles, labels = ax.get_legend_handles_labels()
by_label = dict(zip(labels, handles))
ax.legend(by_label.values(), by_label.keys(), loc='upper right', fontsize=10, framealpha=0.9)

# 紧凑布局
plt.tight_layout()

