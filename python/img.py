# ==========================================
# 2. 画图设置
# ==========================================
# 创建画布，设置大小和分辨率(dpi=300适合论文打印)
fig, ax = plt.subplots(figsize=(12, 8), dpi=150)

# 为6辆车分配6种不同的颜色
colors = ['#1f77b4', '#ff7f0e', '#2ca02c', '#d62728', '#9467bd', '#8c564b']

# ==========================================
# 3. 绘制节点 (散点图)
# ==========================================
for node, (lon, lat) in locations.items():
    if node == 0:
        # 配送中心：红色五角星，调大尺寸
        ax.scatter(lon, lat, c='red', s=250, marker='*', zorder=5, label='配送中心')
        # 在五角星旁边标注'0'
        ax.text(lon + 0.003, lat + 0.003, '0', fontsize=14, fontweight='bold')
    else:
        # 客户点：蓝色圆点
        ax.scatter(lon, lat, c='gray', s=40, zorder=4)
        # 在圆点旁边标注客户ID
        ax.text(lon + 0.002, lat + 0.002, str(node), fontsize=10, color='black')

# ==========================================
# 4. 绘制带箭头的路线
# ==========================================
for vehicle_id, route in enumerate(routes):
    color = colors[vehicle_id % len(colors)]
    
    # 遍历路线中的每一段 (例如 0->25, 25->3)
    for i in range(len(route) - 1):
        start_node = route[i]
        end_node = route[i + 1]
        
        start_lon, start_lat = locations[start_node]
        end_lon, end_lat = locations[end_node]
        
        # 使用 annotate 绘制带有箭头的线
        # shrinkA 和 shrinkB 用于控制箭头连线与节点中心的留白，避免线穿透节点
        ax.annotate('',
                    xy=(end_lon, end_lat),           # 箭头终点
                    xytext=(start_lon, start_lat),   # 箭头起点
                    arrowprops=dict(
                        arrowstyle="-|>",            # 箭头样式
                        color=color,                 # 路线颜色
                        lw=1.8,                      # 线条宽度
                        shrinkA=8,                   # 距离起点的空白距离
                        shrinkB=8,                   # 距离终点的空白距离
                        alpha=0.8                    # 透明度
                    ),
                    zorder=3)

# 为了在图例中显示车辆路线（手动添加占位图例）
for vehicle_id in range(len(routes)):
    ax.plot([], [], color=colors[vehicle_id], lw=1.8, label=f'冷藏车 {vehicle_id + 1} 路线')

# ==========================================
# 5. 图表美化与输出
# ==========================================
#大河四季冷链物流公司车辆配送优化结果
ax.set_title('', fontsize=16, fontweight='bold', pad=15)
ax.set_xlabel('经度 (Longitude)', fontsize=12)
ax.set_ylabel('纬度 (Latitude)', fontsize=12)

# 添加背景网格线
ax.grid(True, linestyle='--', alpha=0.5)

# 合并并显示图例
handles, labels = ax.get_legend_handles_labels()
by_label = dict(zip(labels, handles))
ax.legend(by_label.values(), by_label.keys(), loc='upper right', fontsize=10, framealpha=0.9)

# 紧凑布局
plt.tight_layout()

